// Operadores Aritméticos (Matemáticos)

let salario = 1000

// + , - , * , / , ** (exponencial)

console.log(salario + salario)
console.log(salario - salario)
console.log(salario * salario)
console.log(salario / salario)
console.log(salario ** salario)

